const fs = require("fs-extra");
const path = require("path");
const chalk = require("chalk");
const ora = require("ora");

/**
 * Export knowledge base in various formats
 */
async function exportKnowledgeBase(options = {}) {
  const cwd = process.cwd();
  const aiDir = path.join(cwd, ".ai");
  const format = options.format || "markdown";
  const output = options.output || `knowledge-base.${getExtension(format)}`;

  // Check if .ai directory exists
  if (!(await fs.pathExists(aiDir))) {
    console.log(chalk.red("\nâŒ No .ai/ directory found.\n"));
    console.log(chalk.gray("   Run: npx create-ai-chat-context init\n"));
    return;
  }

  const spinner = ora(`Exporting knowledge base as ${format}...`).start();

  try {
    const outputPath = path.join(cwd, output);

    // Check if output file already exists
    if ((await fs.pathExists(outputPath)) && !options.force) {
      spinner.fail("Export file already exists");
      console.log(
        chalk.yellow(
          `\nâš ï¸  ${output} already exists. Use --force to overwrite.\n`
        )
      );
      return;
    }

    switch (format.toLowerCase()) {
      case "markdown":
      case "md":
        await exportMarkdown(aiDir, outputPath);
        break;
      case "json":
        await exportJSON(aiDir, outputPath);
        break;
      case "html":
        await exportHTML(aiDir, outputPath);
        break;
      default:
        spinner.fail("Unknown format");
        console.log(chalk.red(`\nâŒ Unknown format: ${format}\n`));
        console.log(chalk.gray("   Supported formats: markdown, json, html\n"));
        return;
    }

    spinner.succeed(`Exported to ${output}`);

    console.log(chalk.green("\nâœ… Knowledge base exported!\n"));
    console.log(chalk.bold("ðŸ“„ Output:\n"));
    console.log(`   File: ${chalk.cyan(output)}`);
    console.log(`   Format: ${chalk.cyan(format)}`);
    console.log();

    console.log(chalk.bold("ðŸ’¡ What to do with it:\n"));
    if (format === "markdown" || format === "md") {
      console.log("   â€¢ Share with team members");
      console.log("   â€¢ Create backup");
      console.log("   â€¢ Convert to other formats");
      console.log("   â€¢ Read offline");
    } else if (format === "json") {
      console.log("   â€¢ Import into other tools");
      console.log("   â€¢ Process programmatically");
      console.log("   â€¢ Create backups");
    } else if (format === "html") {
      console.log("   â€¢ View in browser");
      console.log("   â€¢ Share as webpage");
      console.log("   â€¢ Print as document");
    }
    console.log();
  } catch (error) {
    spinner.fail("Failed to export");
    throw error;
  }
}

/**
 * Export as Markdown
 */
async function exportMarkdown(aiDir, outputPath) {
  const files = [
    "README.md",
    "architecture.md",
    "technical-decisions.md",
    "conversation-log.md",
    "known-issues.md",
    "next-steps.md",
    "SETUP_GUIDE.md",
    "TOKEN_MANAGEMENT.md",
  ];

  let content = `# Knowledge Base Export\n\n`;
  content += `**Exported:** ${new Date().toISOString()}\n\n`;
  content += `**Generated by:** [create-ai-chat-context](https://www.npmjs.com/package/create-ai-chat-context)\n\n`;
  content += `---\n\n`;

  for (const file of files) {
    const filePath = path.join(aiDir, file);
    if (await fs.pathExists(filePath)) {
      const fileContent = await fs.readFile(filePath, "utf-8");
      content += `# ${file.replace(".md", "")}\n\n`;
      content += fileContent;
      content += `\n\n---\n\n`;
    }
  }

  await fs.writeFile(outputPath, content, "utf-8");
}

/**
 * Export as JSON
 */
async function exportJSON(aiDir, outputPath) {
  const files = [
    "README.md",
    "architecture.md",
    "technical-decisions.md",
    "conversation-log.md",
    "known-issues.md",
    "next-steps.md",
    "SETUP_GUIDE.md",
    "TOKEN_MANAGEMENT.md",
  ];

  const data = {
    exported: new Date().toISOString(),
    generator: "create-ai-chat-context",
    version: "0.5.0",
    files: {},
  };

  for (const file of files) {
    const filePath = path.join(aiDir, file);
    if (await fs.pathExists(filePath)) {
      const content = await fs.readFile(filePath, "utf-8");
      const stats = await fs.stat(filePath);
      
      data.files[file] = {
        content,
        size: stats.size,
        modified: stats.mtime.toISOString(),
        lines: content.split("\n").length,
        words: content.split(/\s+/).filter((w) => w.length > 0).length,
      };
    }
  }

  await fs.writeFile(outputPath, JSON.stringify(data, null, 2), "utf-8");
}

